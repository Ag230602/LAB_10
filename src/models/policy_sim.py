"""
Policy & Intervention Simulation Module
─────────────────────────────────────────
Simulates the projected impact of five evidence-based public-health
interventions on overdose mortality using a compartmental + Monte Carlo
approach.

Interventions supported
───────────────────────
  naloxone_distribution   – increases naloxone accessibility → reduces fatal OD
  treatment_expansion     – expands MAT/MOUD capacity → reduces active users
  harm_reduction          – syringe programs, safe use sites → reduces OD rate
  law_enforcement         – drug seizures, arrests → short-term supply shock
  prevention_education    – school/community programs → long-term demand reduction

Model
─────
For each intervention, a logistic effect curve determines the monthly
reduction in overdose rate:

  effect(t) = efficacy_max / (1 + exp(-k * (t - inflection)))

Monte Carlo uncertainty bands are generated by sampling:
  - efficacy_max ~ N(μ, σ)
  - policy implementation lag ~ Uniform(lag_min, lag_max) months
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple

import numpy as np
import pandas as pd


# ── Intervention parameter library ───────────────────────────────────────────
