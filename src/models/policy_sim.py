    k: float                   # logistic growth rate
    lag_min: int               # minimum implementation lag (months)
    lag_max: int               # maximum implementation lag (months)
    cost_per_year_m: float     # estimated annual cost in $M
    evidence_level: str        # HIGH / MODERATE / LOW


